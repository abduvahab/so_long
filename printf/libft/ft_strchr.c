#include "libft.h"

char	*ft_strchr(char *s, int c)
{	
	int	i;

	i = 0;
	while (s[i] != (char)c && s[i] != 0)
	{
		i++;
	}
	if (s[i] == (char)c)
		return (s + i);
	return (0);
}
