#include "libft.h"

int	ft_lstsize(t_list *lst)
{
	int	n;

	n = 0;
	if (!lst)
		return (0);
	else
	{
		n = 1;
		while (lst->next)
		{	
			n++;
			lst = lst->next;
		}
		return (n);
	}
}
